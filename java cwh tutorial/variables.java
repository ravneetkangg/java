public class variables {
    public static void main(String[] args){
        //  variables in java can contain $ and _ can start with them also
        //  All other rules are same as other languages

        int n=69;
        System.out.println(n);

        String name= "ravneet";    // in datatype String, s is capital
        System.out.println(name);

        float dec=33.33f;        // writing f is necessary in declaration of a float
        System.out.println(dec);

        boolean isadult=false;   // 0 or 1 is not applicable
        System.out.println(isadult);

        char symbol=48;
        char symbol2='U';
        System.out.println(symbol);
        System.out.println(symbol2);

    }
}
